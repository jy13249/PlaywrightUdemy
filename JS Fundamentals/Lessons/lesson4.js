// Relational or comparison operators

// > - more than
// < - less than
// >= - more than equal
// <= - less than equal

var result = 10 > 5
console.log(result)

// Equality operators

var x = 1
console.log(x == "1") //loose comparison(comparing value)
console.log(x === "1") //strict comparison(comparing value and data type of value)
console.log(x === 1)